#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*cur;

	if (!lst)
		return (0);
	i = 1;
	cur = lst;
	while (cur->next)
	{
		cur = cur->next;
		i++;
	}
	return (i);
}
